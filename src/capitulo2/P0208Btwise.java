package capitulo2;

public class P0208Btwise {

	public static void main(String[] args) {
		
		
		byte a = 1, b = 0;
		
		System.out.println("Valores: a = "+a+" , b = "+b);
		System.out.println("a & b "+(a&b));
		System.out.println("a|b = "+(a|b));
		System.out.println("a^b = "+(a^b));
		System.out.println("~b = "+(~b));
		System.out.println("a<<b = "+(a<<b));
		System.out.println("a<<2 = "+(a<<2));
		System.out.println("-a>>4 = "+(-a>>4));
		System.out.println("-a>>>4 = "+(-a>>>4));
		


		

	}

}
