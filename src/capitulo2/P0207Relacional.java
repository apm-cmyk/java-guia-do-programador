package capitulo2;

public class P0207Relacional {
	
	public static void main(String[] args) {

		int a = 5; int b = 2;
		
		System.out.println("Valores: a = "+a+" , b = "+b);
		System.out.println("a == b = "+(a==b));
		System.out.println("a!=b = "+(a!=b));
		System.out.println("a<b = "+(a<b));
		System.out.println("a>b = "+(a>b));
		System.out.println("a<=b = "+(a<=b));
		System.out.println("a>=b = "+( a>+b));	
		
	}
}
